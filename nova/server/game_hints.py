"""Шпаргалки по играм для глаз: что означают элементы интерфейса.
Подключаются автоматически, когда игра распознана в последних описаниях.
Новая игра = новая запись в словаре (или файл — когда разрастёмся)."""

GAME_HINTS = {
    "rivals": (
        "Контекст: это Marvel Rivals (командный герой-шутер 6на6). "
        "Интерфейс: число у прицела — ПАТРОНЫ в обойме (не счёт!); "
        "проценты у точки — прогресс ЗАХВАТА точки; счёт вида 0-1 "
        "вверху — промежуточный счёт РАУНДОВ, матч продолжается; "
        "киллфид (кто кого убил) — строки справа сверху; портреты "
        "внизу — составы команд; перечёркнутые/серые иконки героев на "
        "экране загрузки — ЗАБАНЕННЫЕ герои, их НЕТ в матче; полоса "
        "снизу у героя — ульта (100% = готова). Режимы: захват точки "
        "или сопровождение груза, играется до N выигранных раундов. "
        "Герои (узнавай по обликам): Эмма Фрост, Джефф (акулёнок), "
        "Луна Сноу, Хела, Пунишер, Дэдпул, Доктор Стрэндж, Магнето, "
        "Шторм, Халк, Старлорд, Псайлок, Человек-паук, Веном, Гроот, "
        "Ракета, Мантис, Адам Уорлок, Мэджик, Мистер Фантастик, "
        "Локи, Тор, Железный человек, Чёрная пантера, Клоак и Кинжал."
    ),
}


def pick_hint(context_text: str) -> str:
    """Выбор шпаргалки по ключу в недавних описаниях экрана."""
    t = context_text.lower()
    for key, hint in GAME_HINTS.items():
        if key in t:
            return hint
    return ""
